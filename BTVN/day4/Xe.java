public class Xe {
	private String TenXe ;
	private String LoaiXe ;
	private int PhanKhoi;
	public String getTenXe() {
		return TenXe;
	}
	public void setTenXe(String newTenXe) {
		TenXe = newTenXe;
	}
	public String getLoaiXe() {
		return LoaiXe;
	}
	public void setLoaiXe(String newLoaiXe) {
		LoaiXe = newLoaiXe;
	}
	public int getPhanKhoi() {
		return PhanKhoi;
	}
	public void setPhanKhoi(int newPhanKhoi) {
		PhanKhoi = newPhanKhoi;
	}
	
}
